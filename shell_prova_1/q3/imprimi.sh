#!/bin/bash

# Definindo as cores
c1='\033[0;31m'  # Vermelho
c2='\033[0;32m'  # Verde
reset='\033[0m'   # Reset

# Poesia
echo -e "${c1}Na noite escura, a lua brilha."
sleep 1
echo -e "${c2}A brisa suave sopra entre as árvores."
sleep 1
echo -e "${c1}As estrelas dançam no céu infinito."
sleep 1
echo -e "${c2}O silêncio da noite é profundo."
sleep 1
echo -e "${c1}O vento carrega os sonhos do mundo."
sleep 1
echo -e "${c2}A alma busca paz no horizonte."
sleep 1
echo -e "${c1}O tempo é uma ponte sem fim."
sleep 1
echo -e "${c2}A vida se refaz em cada amanhecer."
sleep 1
echo -e "${c1}O amor cresce nas sombras da noite."
sleep 1
echo -e "${c2}E a esperança brilha como um farol."
sleep 1
echo -e "${c1}E assim seguimos, com fé no coração."
sleep 1
echo -e "${reset}Fim da poesia."

